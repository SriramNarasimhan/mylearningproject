package com.suntrust.dotcom.beans;

public class MustacheItemsBean {
	private String callouttext;
	private String calloutlink;
	private String linkURL;
	private String target;
	private String icon;
	private String urlParams;
	private String anchorTag;
	

	public String getCallouttext() {
		return callouttext;
	}

	public void setCallouttext(String callouttext) {
		this.callouttext = callouttext;
	}
	
	public String getCalloutlink() {
		return calloutlink;
	}

	public void setCalloutlink(String calloutlink) {
		this.calloutlink = calloutlink;
	}

	public String getLinkURL() {
		return linkURL;
	}

	public void setLinkURL(String linkURL) {
		this.linkURL = linkURL;
	}

	public String getTarget() {
		return target;
	}

	public void setTarget(String target) {
		this.target = target;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}
	
	public String getUrlParams() {
		return urlParams;
	}

	public void setUrlParams(String urlParams) {
		this.urlParams = urlParams;
	}

	public String getAnchorTag() {
		return anchorTag;
	}

	public void setAnchorTag(String anchorTag) {
		this.anchorTag = anchorTag;
	}
}